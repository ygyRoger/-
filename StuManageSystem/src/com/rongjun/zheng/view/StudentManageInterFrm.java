	private void jTable1MousePressed(java.awt.event.MouseEvent evt) {
		// TODO add your handling code here:
		int row = this.jTable1.getSelectedRow();
		this.studentid.setText((String) this.jTable1.getValueAt(row, 0));
		this.studentnumber.setText((String) this.jTable1.getValueAt(row, 1));
		this.name.setText((String) this.jTable1.getValueAt(row, 2));
		this.birthday.setText((String) this.jTable1.getValueAt(row, 3));
		this.nativeplace.setText((String) this.jTable1.getValueAt(row, 4));
		String currentclass = (String) this.jTable1.getValueAt(row, 5);
		int num = this.jComboBox2.getItemCount();
		for (int i = 0; i < num; i++) {
			String studentclass = this.jComboBox2.getItemAt(i).toString();
			if (studentclass.equals(currentclass)) {
				this.jComboBox2.setSelectedIndex(i);
			}
		}
		this.studentRemark.setText((String) this.jTable1.getValueAt(row, 6));
	}

