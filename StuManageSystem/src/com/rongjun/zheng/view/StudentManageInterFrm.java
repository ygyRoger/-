	public void filltable(Student student) {
		DefaultTableModel tm = (DefaultTableModel) this.jTable1.getModel();
		tm.setRowCount(0);
		Connection con = null;
		SchoolClass schoolClass = null;
		try {
			con = dbUtil.getCon();
			ResultSet rs = studentDao.studentList(con, student);
			while (rs.next()) {
				String tableclass = null;
				int num = rs.getInt(6);
				for (int i = 0; i < this.studentclass.getItemCount(); i++) {
					schoolClass = (SchoolClass) this.studentclass.getItemAt(i);
					if (schoolClass.getId() == num) {
						tableclass = schoolClass.getGradeName()
								+ schoolClass.getClassName();
					}
				}
				String[] row = { rs.getInt(1) + "", rs.getString(2),
						rs.getString(3), rs.getString(4), rs.getString(5),
						tableclass, rs.getString(7) };
				tm.addRow(row);
			}
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}

	}

	public void fillclass() {
		this.studentclass.removeAllItems();
		this.jComboBox2.removeAllItems();
		Connection con = null;
		SchoolClass schoolClass = null;
		try {
			con = dbUtil.getCon();
			ResultSet rs = schoolClassDao.classList(con, new SchoolClass());
			while (rs.next()) {
				schoolClass = new SchoolClass();
				schoolClass.setId(rs.getInt(1));
				schoolClass.setClassName(rs.getString(2));
				schoolClass.setGradeID(rs.getInt(3));
				schoolClass.setClassRemark(rs.getString(4));
				schoolClass.setGradeName(rs.getString(6));
				this.studentclass.addItem(schoolClass);
				this.jComboBox2.addItem(schoolClass);
			}
		} catch (Exception e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} finally {
			try {
				dbUtil.close(con);
			} catch (Exception e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			}
		}

	}

